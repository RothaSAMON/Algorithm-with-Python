function createCircle(radius) {
    return {
        radius,
        draw() {
            console.log('draw');
        }
    };
}

const circle1 = createCircle(1);
console.log(circle1);

const circle2 = createCircle(2);
console.log(circle2);

/* 

Factory function, is that you have defined the logic in one place.

We can call this function with the different values or different arguments that we
defined the draw method only 1 place. So, if there is any bug in future it just a 
single place that we need to modify.

*/