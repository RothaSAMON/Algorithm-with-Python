console.log(Math.PI); // 3.141592653589793

console.log(Math.E); // 2.718281828459045

console.log(Math.abs(-5)); // 5
console.log(Math.abs(5)); // 5

//Rounds a number upwards to the nearest integer.
console.log(Math.ceil(4.2)); // 5
console.log(Math.ceil(-4.1)); // -4

//Rounds a number downwards to the nearest integer.
console.log(Math.floor(4.8));  // Output: 4

//Rounds a number to the nearest integer.
console.log(Math.round(4.5));  // Output: 5
console.log(Math.round(4.4));  // Output: 4

//Returns the largest of zero or more numbers.
console.log(Math.max(1, 2, 3, 4, 5));  // Output: 5

//Returns the smallest of zero or more numbers.
console.log(Math.min(1, 2, 3, 4, 5));  // Output: 1

//Returns a pseudo-random number between 0 (inclusive) and 1 (exclusive).
console.log(Math.random());  // Output: A random number between 0 and 1

//Returns the square root of a number.
console.log(Math.sqrt(16));  // Output: 4

//Returns the base to the exponent power, that is, base^exponent.
console.log(Math.pow(2, 3));  // Output: 8

//Returns the sign of a number, indicating whether the number is positive, negative, or zero.
console.log(Math.sign(-5));  // Output: -1
console.log(Math.sign(0));   // Output: 0
console.log(Math.sign(5));   // Output: 1
